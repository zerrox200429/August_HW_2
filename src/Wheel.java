public class Wheel {
    private  int size;
    private String mersedes;

    public Wheel(int size, String mersedes) {
        this.setSize(size);
        this.setMersedes(mersedes);
    }

    public int getSize() {
        return size;
    }

    public void setSize(int size) {
        this.size = size;
    }

    public String getMersedes() {
        return mersedes;
    }

    public void setMersedes(String mersedes) {
        this.mersedes = mersedes;
    }
}
